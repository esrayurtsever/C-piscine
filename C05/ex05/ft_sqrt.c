int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		else
		{
			i++;
		}
	}
	return (0);
}
int main()
{
	printf("%d",ft_sqrt(100));
	return 0;
}
